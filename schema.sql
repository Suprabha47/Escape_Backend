-- Enable UUID generation
create extension if not exists "pgcrypto";


-- ── users ──────────────────────────────────────────────────
-- Minimal user record. In production, hook this to Supabase Auth
-- via a trigger on auth.users so rows are created automatically.

create table if not exists public.users (
  id          uuid primary key default gen_random_uuid(),
  created_at  timestamptz not null default now()
);

comment on table public.users is
  'One row per Escape user. Extend with email/display_name when you add Auth.';


-- ── sessions ───────────────────────────────────────────────
-- One row per task-breaking session. steps_json holds the full
-- blueprint array so we never need a separate steps table for
-- basic history queries.

create table if not exists public.sessions (
  id            uuid primary key default gen_random_uuid(),
  user_id       uuid not null references public.users(id) on delete cascade,
  goal          text not null,
  steps_json    jsonb not null default '[]',   -- full micro_steps array
  energy_level  smallint check (energy_level between 1 and 10),
  low_power_mode boolean not null default false,
  five_second_start text,
  stuck_count   smallint not null default 0,   -- how many times user got stuck
  created_at    timestamptz not null default now(),
  completed_at  timestamptz                    -- null = in-progress
);

create index if not exists sessions_user_id_idx
  on public.sessions(user_id);

create index if not exists sessions_completed_at_idx
  on public.sessions(user_id, completed_at)
  where completed_at is not null;

comment on column public.sessions.steps_json is
  'Full micro_steps array as generated by Claude. Stored as JSONB for cheap reads.';

comment on column public.sessions.completed_at is
  'Set when all steps are marked done. NULL means session is still active.';


-- ── streaks ────────────────────────────────────────────────
-- One row per user. Upserted (never inserted twice) by the
-- recalculate_streak() function whenever a session completes.

create table if not exists public.streaks (
  user_id         uuid primary key references public.users(id) on delete cascade,
  current_streak  int not null default 0,
  longest_streak  int not null default 0,
  last_completed  date,   -- calendar date of last completed session (UTC)
  updated_at      timestamptz not null default now()
);

comment on table public.streaks is
  'Streak counters per user. Recalculated on every session completion.';


-- ── Row Level Security ─────────────────────────────────────
-- These policies assume you will eventually add Supabase Auth.
